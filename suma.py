def sumar(x, y):
    return x + y

def restar(x, y):
    return  x - y


def multiplicar(x, y):
    return x * y

def dividir(x, y):
    return x / y



if __name__ == '__main__':
    print(sumar(5, 3))
    print(restar(5, 3))
    print(multiplicar(5, 3))
    print(dividir(5, 3))
    print(dividir(5, 0))

